SSTACK	SEGMENT STACK
		DW 64 DUP(?)
SSTACK	ENDS
;0809选择IOY0端口
ADC0809 EQU 0600H

;0832选择IOY3端口
DAC0832 EQU 06C0H

CODE SEGMENT
	ASSUME CS:CODE 

START:
	   MOV AX,00H
	   MOV DX, ADC0809
	   MOV CX,0FH
	   ;MOV AL,00H
AA1:
	   ;三角波
	   OUT DX,AL
	   CALL DELAY
	   INC AL
	   CMP AL,0FFH
	   JNE AA1

AA3:	   
	   OUT DX,AL
	   CALL DELAY
	   DEC AL
	   CMP AL,00H
	   JNE AA3
           JMP AA1


OVER:
	   MOV AL,00H
	   OUT DX,AL
	   CALL DELAY
	   JMP OVER
	   
DELAY: PUSH CX
       MOV  CX,0F00H
AA2:   PUSH AX
       POP  AX
	   LOOP AA2
   	   POP  CX
	   RET
	   
CODE ENDS
	 END START